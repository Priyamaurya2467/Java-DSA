package com.maurya;

import java.util.Scanner;

public class SumTillEnds {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter the number");
        int n = sc.nextInt();
        int sum = 0;
        while(true) {
            sum = sum + n;
            if (n == '0') {
                break;
            }
            else{
                break;

            }
        }
    }
}
