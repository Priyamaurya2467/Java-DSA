public class Opreators {
    public static void main(String[] args) {
        int a = 6;
        int b = 1;
        System.out.println("AND Opreator");
        System.out.println(a&b);
        System.out.println("OR Opreator");
        System.out.println(a|b);
        System.out.println("XOR Opreator");
        System.out.println(a^b);
        System.out.println("Left Shift Opreator");
        System.out.println(a<<b);
        System.out.println("Right Shift Opreator");
        System.out.println(a>>b);


    }
}

